/**
  * @brief  This function is executed in case of error occurrence.
  * @param  file: The file name as string.
  * @param  line: The line in file as a number.
  * @retval None
  * 
  * Waits for 2s while blinking the LED at 20Hz to allow the user to 
  * plug in the USB connector,
  * Prints out the error message using the USB/serial ACM interface.
  * Keeps blinking the LED at 20Hz in an infinite loop 
  */
void _Error_Handler(char *file, int line)
{
  /* USER CODE BEGIN Error_Handler_Debug */
  /* User can add his own implementation to report the HAL error return state */

  char msgbuf[128];     //AO!
  int i;

  //Wait for 2-3s (to allow time for connecting the USB connector.)
  for (i=0; i<20;++i){    // Blink LED at 20Hz for 2s
    LED_ON();
    HAL_Delay(10);
    LED_OFF();
    HAL_Delay(40);
  } 

  //USB serial transmit file name and line number:
  sprintf(msgbuf, "Error Handler. File:%s, Line:%d\r\n",file, line);
  CDC_Transmit_FS((uint8_t *) msgbuf, strlen(msgbuf));
  
  while(1)
  {
    // Blink LED at 20Hz.
    LED_ON();
    HAL_Delay(10);
    LED_OFF();
    HAL_Delay(40);
  }
  /* USER CODE END Error_Handler_Debug */
}

