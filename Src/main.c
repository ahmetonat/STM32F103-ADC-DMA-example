/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  int i;                //AO!
  char msgbuf[128];     //AO!

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USB_DEVICE_Init();
  MX_ADC1_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */

  ADC_FLAG=FALSE;
  
  i=0;
  LED_ON();


  HAL_Delay(500);
  LED_OFF();

  sprintf(msgbuf, "Starting device.\r\n");
  CDC_Transmit_FS((uint8_t *) msgbuf, strlen(msgbuf));

  HAL_Delay(500);


  HAL_TIM_Base_Start(&htim3); //AO!: Start the timer.
  
  //The link between hadc1 and hdma_adc1 was completed in stm32f1xx_hal_msp.c:
  HAL_ADC_Start_DMA(&hadc1, adcBuf, ADC_BUFLEN); //Link DMA to ADC1

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  while (1)
  {

    if (ADC_FLAG==TRUE){
      ADC_FLAG=FALSE;
      ++i;
      LED_ON();  //After ADC has been completed.

      sprintf(msgbuf, "%d: ADC1=%d, ADC2=%d, ADC3=%d\r\n", i,
	      (int)adcBuf[0], (int)adcBuf[1], (int)adcBuf[2]);
      CDC_Transmit_FS((uint8_t *) msgbuf, strlen(msgbuf));

      LED_OFF();
      
    } 
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */

}

